public class Veiculo {
    private Integer id;
    private String placa;
    private String nome;
    private Integer ano;
    private Integer modelo;

    public Veiculo(Integer id, String placa, String nome, Integer ano, Integer modelo) {
    this.id = id;
    this.placa = placa;
    this.nome = nome;
    this.ano = ano;
    this.modelo = modelo;
    }
}
